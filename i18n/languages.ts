const ALL_TRANSLATIONS: Record<LanguageCode, TranslationMap> = {
  en,
  es,
  fr,
  ar,
  zh,
  de,
};

export function getTranslation(lang: LanguageCode): TranslationMap {
  return ALL_TRANSLATIONS[lang] || ALL_TRANSLATIONS.en;
}

export function translate(template: string, values?: Record<string, string | number>): string {
  if (!values) return template;
  let result = template;
  for (const [key, value] of Object.entries(values)) {
    result = result.replace(`{${key}}`, String(value));
  }
  return result;
}
